def sumar(op1,op2):
    print(f"El resultado del cálculo es: {op1 + op2}")

def restar(op1,op2):
    print(f"El resultado del cálculo es: {op1 - op2}")

def multiplicar(op1,op2):
    print(f"El resultado del cálculo es: {op1 * op2}")

def dividir(dividendo,divisor):
    print(f"El resultado del cálculo es: {dividendo / divisor}")

def potencia(base,exponente):
    print(f"El resultado del cálculo es: {base ** exponente}")

def redondear(numero):
    print(f"El resultado del cálculo es: {round(numero)}")